# backend/app/database/models_predictions.py
"""
Two new tables:

FBrefFixtures   — upcoming scheduled matches (next 3-5 days)
                  populated by scrape_fixtures.py (daily lightweight scrape)

PredictionLog   — ATHENA predictions generated by batch-predict
                  validated to hit/miss by batch-validate after scrape
"""
from __future__ import annotations

from datetime import datetime

from sqlalchemy import Column, String, Float, Boolean, DateTime, Date, Integer, Text
from sqlalchemy.ext.declarative import declarative_base

Base = declarative_base()


class FBrefFixture(Base):
    __tablename__ = "fbref_fixtures"

    id          = Column(Integer, primary_key=True, autoincrement=True)
    league_code = Column(String,  nullable=False, index=True)
    home_team   = Column(String,  nullable=False)
    away_team   = Column(String,  nullable=False)
    match_date  = Column(Date,    nullable=False, index=True)
    match_time  = Column(String,  nullable=True)   # e.g. "20:45" if available
    scraped_at  = Column(DateTime, default=datetime.utcnow)

    def __repr__(self):
        return f"<FBrefFixture {self.league_code} {self.home_team} vs {self.away_team} {self.match_date}>"


class PredictionLog(Base):
    __tablename__ = "prediction_log"

    id              = Column(Integer, primary_key=True, autoincrement=True)

    # Match identity
    league_code     = Column(String,  nullable=False, index=True)
    home_team       = Column(String,  nullable=False)
    away_team       = Column(String,  nullable=False)
    match_date      = Column(Date,    nullable=False, index=True)

    # ATHENA prediction
    market          = Column(String,  nullable=False)   # e.g. "O1.5", "U3.5/4.5"
    confidence      = Column(String,  nullable=False)   # HIGH / MEDIUM / LOW
    corridor_low    = Column(Float,   nullable=False)
    corridor_high   = Column(Float,   nullable=False)
    lean            = Column(String,  nullable=False)   # over / under / balanced
    confidence_score = Column(Float,  nullable=True)
    applied_modules = Column(Text,    nullable=True)    # JSON string
    explanations    = Column(Text,    nullable=True)    # JSON string

    # Inputs used
    p_two_plus      = Column(Float,   nullable=True)
    tempo_index     = Column(Float,   nullable=True)
    sot_proj_total  = Column(Float,   nullable=True)
    support_delta   = Column(Float,   nullable=True)

    # Validation
    status          = Column(String,  default="pending", index=True)
    # pending → awaiting result
    # hit     → prediction was correct
    # miss    → prediction was wrong
    # void    → match not found in snapshot after scrape

    actual_score    = Column(String,  nullable=True)    # e.g. "2-1"
    actual_total    = Column(Integer, nullable=True)    # total goals
    evaluated_at    = Column(DateTime, nullable=True)

    # Meta
    predicted_at    = Column(DateTime, default=datetime.utcnow)

    def __repr__(self):
        return (
            f"<PredictionLog {self.league_code} "
            f"{self.home_team} vs {self.away_team} "
            f"{self.match_date} [{self.status}]>"
        )
